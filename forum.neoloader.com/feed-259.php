The requested feature have been added<br /><br />there is a setting: "Bandwidth/IPFilter" that takes a string,<br />1. a filename only -&gt; pointing to an ipfilter file in the config directory<br />2. a full path pointing to an ipfilter file at a arbitrary location<br />3. a url pointing to an plaintex or compressed ipfilter download<br /><br />In case 3. Neo loader will check on each start if the file on the server has changed (HEAD request) and if so download it (GET request) and replace the old one. Howe ever no more than one update a day is done, even if the file has changed.<br /><br /><br />There is also a setting "Bandwidth/UseIPv6" that takes a boolean (true/false) to disable IPv6 Support since current ipfilter files does not support IPv6 addresses.<p>Statistics: Posted by <a href="http://forum.neoloader.com/memberlist.php?mode=viewprofile&amp;u=561">DavidXanatos</a> — Sun May 19, 2013 8:52 am</p><hr />
