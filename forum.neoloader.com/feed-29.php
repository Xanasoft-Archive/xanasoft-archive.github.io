NeoLoaders BitTorrent and Ed2k support is state of the art and we support all relevant protocol extensions for this networks.<br /><br />About file hosting, NeoLoader supports 2 modes of operation the classical Split archive / Solid file like JDownloader and a new hash based exchangeable multi part scheme that is similar to how P2P works, just that the parts are transfered to and from hosters and not other peers.<br />So you enter a magnet link with a Neo hash and NeoLoader can find all needed parts using a decentralized DHT/Kademlia like infrastructure and download all needed parts from hosters.<br />The way the parts are uploaded makes them perfectly interchangeable like in P2P, it also provides a keyword based download search, it basically gives you a P2P behavior with the security and speed of file Hosters.<br /><br />NeoLoader is also capable of using its Kademlia/DHT network to find hashes for a given file for different networks, so that if it happens that all or some needed hoster links are down NeoLoader can download the missing parts from ed2k or BT. That also enables to download one file form Ed2k and BT at the same time....<br /><br />This capability is also used in the opposite direction to aid P2P downloads by caching often requested parts on hosters such that other neos can download them faster from there.<br /><br />And finally we have our new Anonymous filesharing system NeoShare, so to say Neo is an AIO software that in addition has a kind of "next gen anonymous BT" <img class="smilies" src="http://forum.neoloader.com/images/smilies/biggrin.png" alt=":D" title=":D" /><br /><br />There is also a longer interview available where we explain Neos novel feature in much more detail: <a href="http://www.emule-mods.it/forum/index.php?topic=2386.0" class="postlink">Intervista agli sviluppatori di NeoLoader</a><p>Statistics: Posted by <a href="http://forum.neoloader.com/memberlist.php?mode=viewprofile&amp;u=561">DavidXanatos</a> — Mon Dec 16, 2013 10:55 pm</p